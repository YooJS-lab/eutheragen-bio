const html = document.documentElement;
const toggleButton = document.getElementById('langToggle');
const translatableNodes = document.querySelectorAll('[data-i18n]');

function applyLanguage(lang) {
  const dict = translations[lang];
  html.lang = lang;
  translatableNodes.forEach((node) => {
    const key = node.dataset.i18n;
    if (dict[key]) {
      node.textContent = dict[key];
    }
  });
  toggleButton.textContent = lang === 'en' ? 'KO' : 'EN';
  localStorage.setItem('eutheragen-lang', lang);
}

const savedLang = localStorage.getItem('eutheragen-lang') || 'en';
applyLanguage(savedLang);

toggleButton.addEventListener('click', () => {
  const next = html.lang === 'en' ? 'ko' : 'en';
  applyLanguage(next);
});

document.getElementById('year').textContent = new Date().getFullYear();
