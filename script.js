const html = document.documentElement;
const langToggle = document.getElementById('langToggle');
const menuToggle = document.getElementById('menuToggle');
const mobileMenu = document.getElementById('mobileMenu');
const yearEl = document.getElementById('year');

yearEl.textContent = new Date().getFullYear();

function applyLanguage(lang) {
  const content = translations[lang];
  if (!content) return;

  document.querySelectorAll('[data-i18n]').forEach((el) => {
    const key = el.getAttribute('data-i18n');
    if (content[key]) el.textContent = content[key];
  });

  html.lang = lang === 'ko' ? 'ko' : 'en';
  localStorage.setItem('eutheragen-lang', lang);

  document.querySelectorAll('.lang-pill').forEach((pill) => {
    pill.classList.toggle('active', pill.dataset.langLabel.toLowerCase() === lang);
  });
}

langToggle?.addEventListener('click', () => {
  const current = localStorage.getItem('eutheragen-lang') || 'en';
  applyLanguage(current === 'en' ? 'ko' : 'en');
});

menuToggle?.addEventListener('click', () => {
  const expanded = menuToggle.getAttribute('aria-expanded') === 'true';
  menuToggle.setAttribute('aria-expanded', String(!expanded));
  mobileMenu.hidden = expanded;
});

document.querySelectorAll('.mobile-menu a').forEach((link) => {
  link.addEventListener('click', () => {
    mobileMenu.hidden = true;
    menuToggle.setAttribute('aria-expanded', 'false');
  });
});

const observer = new IntersectionObserver((entries) => {
  entries.forEach((entry) => {
    if (entry.isIntersecting) {
      entry.target.classList.add('visible');
      observer.unobserve(entry.target);
    }
  });
}, { threshold: 0.12 });

document.querySelectorAll('.reveal').forEach((el) => observer.observe(el));

